# Сортировка слиянием
def merge_sort(arr):
   
    # Базовый случай: массивы длиной 0 или 1 уже отсортированы
    # Условие остановки рекурсии
    if len(arr) <= 1:
        return arr
    
    # Шаг 1: Разделение
    mid = len(arr) // 2
    left_half = arr[:mid]    # Левая половина
    right_half = arr[mid:]   # Правая половина
    
    # Шаг 2: Рекурсивная сортировка половин
    # будем разбивать нащ массив на половины до тех пор покак длины подмассивов будту равны 1
    left_sorted = merge_sort(left_half)
    right_sorted = merge_sort(right_half)
    
    # Шаг 3: Объединяем отсортированные половины с помощью функции merge
    return merge(left_sorted, right_sorted)

# Эта функция где происходит настоящее сортирование
def merge(left, right):
    
    result = []         # Результирующий массив
    i = j = 0           # i указывает на текущий элемент в левом массиве
                        # j указывает на текущий элемент в правом массиве
    #Шаг альфа
    # Сравниваем элементы из обоих массивов и добавляем меньший в результат
    while i < len(left) and j < len(right): # пока в обоих массивах есть элементы для сравнения
        if left[i] <= right[j]: # сравниваем текущие элементы из левого и правого массивов
            result.append(left[i]) 
            i += 1 # Увеличиваем соответствующий указатель после добавления элемента
        else:
            result.append(right[j])
            j += 1 # Увеличиваем соответствующий указатель после добавления элемента

    #Эти циклы добавляют оставшиеся элементы из того массива, где еще остались элементы
    #Поскольку исходные массивы уже отсортированы, мы можем просто добавить все оставшиеся элементы в конец
    # Добавляем оставшиеся элементы из левого массива (если есть)
    while i < len(left):
        result.append(left[i])
        i += 1
    
    # Добавляем оставшиеся элементы из правого массива (если есть)
    while j < len(right):
        result.append(right[j])
        j += 1
    
    return result


# Пример использования
data = [329, 457, 657, 839, 436, 720, 355]
print("Исходный массив:", data)
sorted_data = merge_sort(data.copy())
print("Отсортированный массив:", sorted_data)

# Демонстрация на маленьком массиве
print("\n--- Демонстрация процесса ---")
